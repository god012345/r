# 6. Program to Implement String Manipulation Functions in R
text <- "Hello World"
# Convert to uppercase
cat("Uppercase:", toupper(text), "\n")
# Convert to lowercase
cat("Lowercase:", tolower(text), "\n")
# Substring
cat("Substring (1-5):", substr(text, 1, 5), "\n")
# Concatenate
cat("Concatenate:", paste(text, "R Programming"), "\n")
